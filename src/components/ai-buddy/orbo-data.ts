export function getRoleAwareCvComment(
  role: CvReaderRole,
  key: string,
  genericComments: Record<string, string[]>,
  roleComments: Record<CvReaderRole, Partial<Record<string, string[]>>>,
): string | null {
  const contextual = roleComments[role]?.[key];
  if (contextual?.length) return pickRandom(contextual);
  const generic = genericComments[key];
  return generic?.length ? pickRandom(generic) : null;
}

export function getCvSectionComment(sectionId: string, role: CvReaderRole): string | null {
  return getRoleAwareCvComment(role, sectionId, CV_SECTION_COMMENTS, CV_ROLE_SECTION_COMMENTS);
}

export function getCvItemComment(el: Element, role: CvReaderRole): string | null {
  const item = el.closest<HTMLElement>("[data-orbo-cv-item]")?.dataset.orboCvItem;
  if (!item) return null;
  return getRoleAwareCvComment(role, item, CV_ITEM_COMMENTS, CV_ROLE_ITEM_COMMENTS);
}

export function getCvActionComment(el: Element, role: CvReaderRole): string | null {
  const action = el.closest<HTMLElement>("[data-orbo-cv-action]")?.dataset.orboCvAction;
  if (!action) return null;
  return getRoleAwareCvComment(role, action, CV_ACTION_COMMENTS, CV_ROLE_ACTION_COMMENTS);
}

